    private void updateSponzorTip() {
        if(rbPL.isSelected()) {
            pnlFLPL.setBorder(javax.swing.BorderFactory.createTitledBorder("Podaci o Pravnom licu"));
            txtPIB.setVisible(true);
            lblPIB.setVisible(true);
            lblRegBr.setText("Registarski broj:");
        } else {
            pnlFLPL.setBorder(javax.swing.BorderFactory.createTitledBorder("Podaci o Fiziƒçkom licu"));
            txtPIB.setVisible(false);
            lblPIB.setVisible(false);
            lblRegBr.setText("JMBG:");
        }
        
        txtKontakt.setText("");
        txtNaziv.setText("");
        txtPIB.setText("");
        txtRegBr.setText("");
    }


