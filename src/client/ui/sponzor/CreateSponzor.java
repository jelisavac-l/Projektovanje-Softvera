    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        if(rbPL.isSelected())
        {
            PravnoLice pl = new PravnoLice(
                    txtRegBr.getText(), 
                    txtPIB.getText(), 
                    null, 
                    txtNaziv.getText(), 
                    txtKontakt.getText());
            try {
                KontrolerSponzor.createPL(pl);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(CreateSponzor.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } else {
            FizickoLice fl = new FizickoLice(txtRegBr.getText(), 
                    null, 
                    txtNaziv.getText(), 
                    txtKontakt.getText());
            
            try {
                KontrolerSponzor.createFL(fl);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(CreateSponzor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCreateActionPerformed

